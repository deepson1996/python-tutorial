'''
Operators in python
1. Airthmetic Operators: +, -, *, / etc
2. Comparison Operators: ==, !=, >, <, >=, <= etc
3. Assignment Operators: =, +=, -=, *=, /=, etc
4. Logical Operators: and, or, not
'''

a = 3
b= 4
# Airthmetic operators
print("The value of a + b is ", a+b)
print("The value of a - b is ", a-b)
print("The value of a * b is ", a*b)
print("The value of a / b is ", a/b) # returns float value in / operations
print("===============================")
# Comparison operator 
print("The value of a == b is ", a==b)
print("The value of a != b is ", a!=b)
print("The value of a > b is ", a>b)
print("The value of a < b is ", a<b)
print("The value of a >= b is ", a>=b)
print("The value of a <= b is ", a<=b)
print("===============================")


# Assignment Operators 
c = 34
c += 2 # we can use -=, *=, /= etc in this way
c -= 4
c *= 4
c /= 2 # divide always returns float
print('The value of c is ', c)
print("===============================")

# Logical operators 
bool1 = True
bool2 = False
print("The value of bool1 and bool2 is ", bool1 and bool2)
print("The value of bool1 or bool2 is ", bool1 or bool2)
print("The value of not bool1 is ", not bool1)


print("===============================")




